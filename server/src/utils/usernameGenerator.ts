const getRandomDigitString = () => {
    return String(Math.floor(Math.random() * 10));
};

const getRandomDigitsString = (n: number) => {
    let result = "";
    for (let i = 0; i < n; i++) {
        result += getRandomDigitString();
    }
    return result;
};

const capitalize = (word: string) => {
    return word.charAt(0).toUpperCase() + word.slice(1);
};

const randomItem = <T>(arr: T[]): T => {
    return arr[Math.floor(Math.random() * arr.length)];
};

const generateUsername = () => {
    const animal = randomItem(parts.animals);
    const adjectif = randomItem(parts.adjectives);
    const number = getRandomDigitsString(2);

    return `${animal}${capitalize(adjectif)}${number}`;
};

